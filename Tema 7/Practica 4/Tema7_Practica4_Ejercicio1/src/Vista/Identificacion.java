    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        labelError.setVisible(false);
        ArrayList<javax.swing.JButton> listaBotones = new ArrayList();
        ArrayList<String> listaNumeros = new ArrayList();
        listaBotones.add(bCero);listaNumeros.add("0");
        listaBotones.add(bUno);listaNumeros.add("1");
        listaBotones.add(bDos);listaNumeros.add("2");
        listaBotones.add(bTres);listaNumeros.add("3");
        listaBotones.add(bCuatro);listaNumeros.add("4");
        listaBotones.add(bCinco);listaNumeros.add("5");
        listaBotones.add(bSeis);listaNumeros.add("6");
        listaBotones.add(bSiete);listaNumeros.add("7");
        listaBotones.add(bOcho);listaNumeros.add("8");
        listaBotones.add(bNueve);listaNumeros.add("9");
        for(int x=9; x>-1;x--)
        {
            int electorBoton = (int)(Math.round(Math.random()*10));
            int electorNumero = (int)(Math.round(Math.random()*10));
            while(electorBoton>x)
            {
                electorBoton = (int)(Math.round(Math.random()*10));
            }
            while(electorNumero>x)
            {
                electorNumero = (int)(Math.round(Math.random()*10));
            }
            listaBotones.get(electorBoton).setText(listaNumeros.get(electorNumero));
            listaBotones.remove(electorBoton);
            listaNumeros.remove(electorNumero);
        }
    }//GEN-LAST:event_formWindowOpened

